CREATE OR REPLACE FUNCTION is_admin_user()
RETURNS BOOLEAN
LANGUAGE SQL
STABLE
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM admin_users au
    WHERE au.user_id = auth.uid()
  );
$$;

CREATE OR REPLACE FUNCTION is_commissioner_user()
RETURNS BOOLEAN
LANGUAGE SQL
STABLE
AS $$
  SELECT public.is_commissioner_user(auth.uid());
$$;

CREATE OR REPLACE FUNCTION owns_team(target_team_id BIGINT, target_season_id BIGINT DEFAULT NULL)
RETURNS BOOLEAN
LANGUAGE SQL
STABLE
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM team_owner_users tou
    WHERE tou.user_id = auth.uid()
      AND tou.team_id = target_team_id
      AND tou.active = TRUE
      AND tou.role IN ('owner', 'co_owner')
      AND (tou.season_id IS NULL OR target_season_id IS NULL OR tou.season_id = target_season_id)
  );
$$;

CREATE OR REPLACE FUNCTION public.can_view_league_rosters(target_season_id BIGINT, target_league_code TEXT)
RETURNS BOOLEAN
LANGUAGE SQL
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT
    public.is_admin_user(auth.uid())
    OR EXISTS (
      SELECT 1
      FROM team_owner_users tou
      JOIN season_team_assignments sta
        ON sta.team_id = tou.team_id
        AND sta.season_id = target_season_id
      WHERE tou.user_id = auth.uid()
        AND tou.active = TRUE
        AND tou.role IN ('owner', 'co_owner')
        AND lower(sta.league_code) = lower(target_league_code)
        AND (tou.season_id IS NULL OR tou.season_id = target_season_id)
    );
$$;

GRANT EXECUTE ON FUNCTION public.can_view_league_rosters(BIGINT, TEXT) TO authenticated;

CREATE OR REPLACE VIEW public_available_players AS
SELECT
  sta.season_id,
  sta.league_code,
  p.id AS player_id,
  p.mlb_player_id,
  p.display_name,
  p.primary_position,
  p.eligible_positions,
  mt.abbreviation AS mlb_team_abbreviation
FROM season_team_assignments sta
CROSS JOIN mlb_players p
LEFT JOIN mlb_teams mt ON mt.id = p.mlb_team_id
WHERE p.active = TRUE
  AND NOT EXISTS (
    SELECT 1
    FROM roster_memberships rm
    WHERE rm.season_id = sta.season_id
      AND rm.league_code = sta.league_code
      AND rm.player_id = p.id
      AND rm.released_on IS NULL
  )
GROUP BY
  sta.season_id,
  sta.league_code,
  p.id,
  p.mlb_player_id,
  p.display_name,
  p.primary_position,
  p.eligible_positions,
  mt.abbreviation;

DROP VIEW IF EXISTS public_team_rosters;

CREATE OR REPLACE VIEW public_team_rosters
WITH (security_invoker = true) AS
SELECT
  rm.season_id,
  s.season_number,
  rm.league_code,
  rm.team_id,
  t.name AS team_name,
  rm.player_id,
  p.mlb_player_id,
  p.display_name AS player_name,
  p.primary_position,
  p.eligible_positions,
  mt.abbreviation AS mlb_team_abbreviation,
  rm.acquired_on,
  rm.contract_years,
  rm.acquisition_type
FROM roster_memberships rm
JOIN seasons s ON s.id = rm.season_id
JOIN teams t ON t.id = rm.team_id
JOIN mlb_players p ON p.id = rm.player_id
LEFT JOIN mlb_teams mt ON mt.id = p.mlb_team_id
WHERE rm.released_on IS NULL
  AND public.can_view_league_rosters(rm.season_id, rm.league_code);

