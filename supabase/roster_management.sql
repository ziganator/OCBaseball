CREATE OR REPLACE VIEW public_team_owner_assignments
WITH (security_invoker = true) AS
SELECT
  tou.id,
  tou.user_id,
  COALESCE(tou.owner_email, up.email) AS owner_email,
  COALESCE(tou.owner_name, up.display_name) AS owner_name,
  up.username,
  tou.team_id,
  t.name AS team_name,
  tou.season_id,
  s.season_number,
  s.name AS season_name,
  tou.role,
  tou.active,
  tou.created_at
FROM team_owner_users tou
JOIN teams t ON t.id = tou.team_id
LEFT JOIN user_profiles up ON up.user_id = tou.user_id
LEFT JOIN seasons s ON s.id = tou.season_id;

CREATE TABLE IF NOT EXISTS roster_memberships (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season_id BIGINT NOT NULL REFERENCES seasons(id) ON DELETE CASCADE,
  team_id BIGINT NOT NULL REFERENCES teams(id) ON DELETE CASCADE,
  league_code VARCHAR(20) NOT NULL,
  player_id BIGINT NOT NULL REFERENCES mlb_players(id) ON DELETE CASCADE,
  acquired_on DATE NOT NULL DEFAULT CURRENT_DATE,
  released_on DATE NULL,
  contract_years TEXT NULL,
  acquisition_type VARCHAR(20) NOT NULL DEFAULT 'draft'
    CHECK (acquisition_type IN ('draft', 'add', 'trade', 'admin')),
  release_type VARCHAR(20) NULL
    CHECK (release_type IN ('drop', 'trade', 'admin')),
  created_by UUID NULL REFERENCES auth.users(id),
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
  CONSTRAINT chk_roster_dates CHECK (released_on IS NULL OR released_on >= acquired_on)
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_active_player_claim_by_league
  ON roster_memberships (season_id, league_code, player_id)
  WHERE released_on IS NULL;

CREATE INDEX IF NOT EXISTS ix_roster_memberships_team
  ON roster_memberships (season_id, team_id, released_on);

