CREATE TABLE IF NOT EXISTS mlb_teams (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  mlb_team_id INT NULL,
  abbreviation VARCHAR(10) NOT NULL,
  name VARCHAR(120) NOT NULL,
  active BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
  CONSTRAINT uq_mlb_teams_abbreviation UNIQUE (abbreviation)
);

CREATE TABLE IF NOT EXISTS mlb_players (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  mlb_player_id BIGINT NULL,
  display_name VARCHAR(160) NOT NULL,
  normalized_name VARCHAR(160) NOT NULL,
  bats CHAR(1) NULL CHECK (bats IN ('L', 'R', 'S')),
  throws CHAR(1) NULL CHECK (throws IN ('L', 'R')),
  primary_position VARCHAR(10) NULL,
  eligible_positions TEXT[] NOT NULL DEFAULT '{}',
  mlb_team_id BIGINT NULL REFERENCES mlb_teams(id),
  active BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
  CONSTRAINT uq_mlb_players_provider UNIQUE (mlb_player_id),
  CONSTRAINT uq_mlb_players_normalized UNIQUE (normalized_name)
);

CREATE INDEX IF NOT EXISTS ix_mlb_players_active
  ON mlb_players (active, normalized_name);

